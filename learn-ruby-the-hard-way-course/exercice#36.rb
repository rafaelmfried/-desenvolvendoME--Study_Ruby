# Designing and Debugging

# Every if must have an else

# Never nest if-statements more than two deep and always try to do them one deep.

# Trat if-statement like paragraphs, where each if-elsif-else grouping is like a set of sentences. Put blank lines before and after

# Use good name for the variables.

# Use while loop only for loop forever

# Use for-loop for all other kinds of looping, especiallu if there is a fixed or limited number of things to loop over.